// Math Homework Code
#include <stdio.h>
#include <stdlib.h>

int main() {
  // Declare variables
  int a = 0;
  int b = 0;
  int sum = 0;

  // Ask user for input
  printf("Enter two numbers: ");
  scanf("%d %d", &a, &b);

  // Calculate sum
  sum = a + b;

  // Print result
  printf("The sum of the two numbers is: %d\n", sum);

  return 0;
}
